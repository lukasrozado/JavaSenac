package aula01.calculadora;

public class Multiplicacao{
    private int num1;
    private int num2;
    public Multiplicacao(int num1, int num2){
        this.num1 = num1;
        this.num2 = num2;
    }
    public int CalculadoraMultiplicacao(int num1, int num2){
        return num1 * num2;
    }

}
